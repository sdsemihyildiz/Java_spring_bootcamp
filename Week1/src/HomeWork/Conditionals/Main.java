package HomeWork.Conditionals;

public class Main {
    public static void main(String[] args) {

        int number = 20;
        if (number < 20) {
            System.out.println("Number is lower than 20");
        } else if (number == 20) {
            System.out.println("Number is equal to 20");
        } else {
            System.out.println("Number is higher than 20");
        }
    }
}
